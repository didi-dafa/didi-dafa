exports.ჩაუშვი=function(მოთხ, პასუხ, მომხმარებელი, მოდულები){
    if(!მოთხ.პარამები.ენა){
        console.log('მომე-თარგმანი: ცუდი მოთხოვნა')
        პასუხ.statusCode=400;
        პასუხ.end('ცუდი მოთხოვნა');
        return
    }
    პასუხ.end(JSON.stringify(მოდულები.თარგმანი.მომე(მოთხ.პარამები.ენა)))
} 
