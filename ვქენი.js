var კითხვის_სიმი=require('querystring'),
    ურლ=require('url')
    
exports.ჩაუშვი=function(მოთხ, პასუხ, მომხმარებელი, მოდულები){
    var პარამეტრები = კითხვის_სიმი.parse(ურლ.parse(მოთხ.url).query)
    მოდულები.ნაქნარები.დაამატე(მომხმარებელი, პარამეტრები.რა, პარამეტრები.ხ, პარამეტრები.ჯ)
    პასუხ.end()
} 
