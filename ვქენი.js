var კითხვის_სიმი=require('querystring'),
    ურლ=require('url')
    
var ნაქნარები=[]
exports.ჩაუშვი=function(მოთხ, პასუხ, სესიის_იდ, მომხმარებლები){
    var პარამეტრები = კითხვის_სიმი.parse(ურლ.parse(მოთხ.url).query)
    ნაქნარები.push({
        დრო:new Date().getTime(),
        მქნელი:სესიის_იდ,
        რა:პარამეტრები.რა,
        ხ:პარამეტრები.ხ,
        ჯ:პარამეტრები.ჯ,
    })
    console.log(ნაქნარები);
    პასუხ.end()
} 
